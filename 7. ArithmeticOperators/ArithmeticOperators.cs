namespace _7._Arithmetic_Operators
{
    internal class ArithmeticOperators
    {
        static void Main(string[] args)
        {
            // Addition:

            int a = 5;
            int b = 3;
            int sum = a + b;

            Console.WriteLine("The sum is: " + sum);

            // Division:

            int dividend = 15;
            int divisor = 4;
            int quotient = dividend / divisor;

            Console.WriteLine("The quotient is: " + quotient);
        }
    }
}